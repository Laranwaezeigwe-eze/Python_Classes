class Calculator(object):

    @staticmethod
    def add(num1: int, num2: int) -> int:
        return num1 + num2

    @staticmethod
    def subtract(num1: int, num2: int) -> int:
        return num1 - num2

    @staticmethod
    def multiply(num1: int, num2: int) -> int:
        return num1 * num2

    @staticmethod
    def divide(num1: int, num2: int) -> int:
        return num1 // num2
